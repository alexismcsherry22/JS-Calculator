export const calAdd = (x, y) => {
  return parseFloat(x + y);
}

export const calSub = (x, y) => {
  return parseFloat(x - y);
}

export const calMul = (x, y) => {
  return parseFloat(x * y);
}

export const calDiv = (x, y) => {
  return parseFloat(x / y);
}

export const calSqr = (x) => {
  return parseFloat(Math.sqrt(x));
}